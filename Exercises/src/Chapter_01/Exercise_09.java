package Chapter_01;

/**
 * Introduction to Java Programming 10th Edition by Y Daniel Liang
 * Chapter 1 Exercise 9
 *      (Area and perimeter of a rectangle) Write a program that displays the area and perimeter of a rectangle with the width of 4.5 and height of 7.9 using the following formula:
 *
 *      area = width * height
 *
 * Created by Melissa on 20/04/2017.
 */

public class Exercise_09 {
    public static void main(String[] args) {
        System.out.println("Perimeter: " + ((4.5 * 2) + (7.9 * 2)));
        System.out.println("Area: " + (4.5 * 7.9));
    }
}
